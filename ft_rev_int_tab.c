void	ft_rev_int_tab(int *tab, int size)
{
	int	swap;
	int	i;
	int	j;

	j = 0;
	i = size - 1;
	while (j < size / 2)
	{
		swap = tab[j];
		tab[j] = tab[i];
		tab[i] = swap;
		j++;
		i--;
	}
}
